// Filter to only include exact-length words
export const getWords = (length) => {
  const list = length === 3 ? WORDS_3 : WORDS_4;
  return list.filter((w) => w.length === length);
};

export const getRandomWord = (length) => {
  const words = getWords(length);
  return words[Math.floor(Math.random() * words.length)];
};
